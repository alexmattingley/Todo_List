        About
    </h1>

    <p>adv.
        Old English abutan, earlier onbutan "on the outside of," from on (see on ) + be "by" (see by ) + utan
        "outside," from ut (see out (adv.)). By 13c. it had forced out Old English ymbe, ymbutan for meaning "in
        the neighborhood of." Abouts, with adverbial genitive, still found in hereabouts, etc., probably is a
        northern dialectal form. About face as a military command (short for right about face) is first attested
        1861, American English.</p>
